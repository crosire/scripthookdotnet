namespace GTA.Chrono
{
    /// <summary>
    /// Specifies the day of the week with their numerical value according to ISO 8601.
    /// </summary>
    public enum IsoDayOfWeek
    {
        /// <summary>
        /// Indicates Monday.
        /// </summary>
        Monday = 0,
        /// <summary>
        /// Indicates Tuesday.
        /// </summary>
        Tuesday = 1,
        /// <summary>
        /// Indicates Wednesday.
        /// </summary>
        Wednesday = 2,
        /// <summary>
        /// Indicates Thursday.
        /// </summary>
        Thursday = 3,
        /// <summary>
        /// Indicates Friday.
        /// </summary>
        Friday = 4,
        /// <summary>
        /// Indicates Saturday.
        /// </summary>
        Saturday = 5,
        /// <summary>
        /// Indicates Sunday.
        /// </summary>
        Sunday = 6,
    }
}
